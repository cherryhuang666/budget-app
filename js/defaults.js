function suggestEmojisByName(name) {
  if (!name) return [];
  const n = name.trim();
  if (!n) return [];
  const out = [];
  const seen = new Set();
  // 完全匹配优先（CATEGORY_ICONS 里有就先用）
  if (CATEGORY_ICONS[n]) { out.push(CATEGORY_ICONS[n]); seen.add(CATEGORY_ICONS[n]); }
  // 按关键词子串匹配
  for (const [keyword, emojis] of Object.entries(EMOJI_KEYWORDS)) {
    if (n.includes(keyword) || keyword.includes(n)) {
      for (const e of emojis) {
        if (!seen.has(e)) { seen.add(e); out.push(e); }
      }
    }
  }
  return out.slice(0, 12);
}

